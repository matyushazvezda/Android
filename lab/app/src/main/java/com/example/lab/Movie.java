package com.example.lab;

public class Movie {
    private String title;
    private int year;
    private String director;
    private String genre;
    private double rating;

    public String getTitle() {
        return title;
    }

    public int getYear() {
        return year;
    }

    public String getDirector() {
        return director;
    }

    public String getGenre() {
        return genre;
    }

    public double getRating() {
        return rating;
    }
}

